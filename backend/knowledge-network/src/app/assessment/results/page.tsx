'use client';

import React, { useEffect, useState } from 'react';
import { useRouter } from 'next/navigation';
import { Loader2 } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';

interface Student {
  id: string;
  student_name: string;
  scores: {
    comprehension: number;
    implementation: number;
    integration: number;
  };
}

interface MatchResult {
  matches: string;
  nodes: Student[];
}

export default function ResultsPage() {
  const [matchResult, setMatchResult] = useState<MatchResult | null>(null);
  const [isLoading, setIsLoading] = useState(true);
  const router = useRouter();

  useEffect(() => {
    const fetchResults = async () => {
      try {
        const response = await fetch('/api/assessment/results');
        if (!response.ok) {
          throw new Error('Failed to fetch results');
        }

        const data = (await response.json()) as MatchResult;
        setMatchResult(data);
      } catch (error) {
        console.error('Error fetching results:', error);
      } finally {
        setIsLoading(false);
      }
    };

    fetchResults();
  }, []);

  if (isLoading) {
    return (
      <div className="p-6">
        <div className="flex min-h-[320px] items-center justify-center">
          <Card className="w-full max-w-lg">
            <CardHeader className="text-center">
              <Loader2 className="mx-auto h-8 w-8 animate-spin text-primary" />
              <CardTitle>Finding your study group</CardTitle>
              <CardDescription>Analyzing your profile and peer compatibility...</CardDescription>
            </CardHeader>
          </Card>
        </div>
      </div>
    );
  }

  return (
    <div className="p-6">
      <div className="mx-auto max-w-4xl space-y-6">
        <div className="text-center">
          <h1 className="text-2xl font-bold">Group Matching Complete</h1>
          <p className="mt-2 text-muted-foreground">You have been matched with peers who complement your learning style.</p>
        </div>

        <Card>
          <CardHeader>
            <CardTitle>Match Details</CardTitle>
            <CardDescription>Summary generated by the matching service.</CardDescription>
          </CardHeader>
          <CardContent>
            <p className="whitespace-pre-wrap text-sm text-muted-foreground">{matchResult?.matches ?? 'No match details available.'}</p>
          </CardContent>
        </Card>

        <div className="flex justify-center">
          <Button onClick={() => router.push('/')}>Start Collaborating</Button>
        </div>
      </div>
    </div>
  );
}
